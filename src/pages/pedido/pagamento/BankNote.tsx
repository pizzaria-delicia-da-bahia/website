import { ButtonBankNote } from "@components/pedido/bankNoteButton";
import { usePagamentoPage } from "./Context";

export const BankNote = ({ value }: { value: number }) => {
  const { pagamentoAtual, setPagamentoAtual, addPagamento } =
    usePagamentoPage();
  return (
    <ButtonBankNote
      value={value}
      disabled={value < pagamentoAtual.valor}
      selected={
        pagamentoAtual.tipo === "especie" && pagamentoAtual.trocoPara === value
      }
      click={() => {
        setPagamentoAtual((prev) => ({ ...prev, trocoPara: value }));
        addPagamento({ ...pagamentoAtual, trocoPara: value });
      }}
    />
  );
};
